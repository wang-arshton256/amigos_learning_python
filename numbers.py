addition = 10 + 5           # (+) addition
subtraction = 10 - 5        # (-) subtraction
multiplication = 10 * 5     # (*) multiplication
division = 10 / 5           # (/) division
# (%) modulas means how many times a number is divisible by the other and the remainder it leaves.
mod = 10 % 5

print(addition)
print(subtraction)
print(multiplication)
print(division)
print(mod)
